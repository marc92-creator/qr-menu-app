/**
 * Find the best emoji representation for a menu item
 * Uses multiple matching strategies for best results
 */
export function findBestImage(itemName: string, categoryName?: string): string {
  const name = itemName.toLowerCase().trim();
  const category = categoryName?.toLowerCase().trim() || '';

  // 1. Exact match
  if (imageLibrary[name]) {
    return imageLibrary[name];
  }

  // 2. Contains check (longest match first for better specificity)
  const sortedKeys = Object.keys(imageLibrary).sort((a, b) => b.length - a.length);
  for (const key of sortedKeys) {
    if (name.includes(key)) {
      return imageLibrary[key];
    }
  }

  // 3. Reverse contains (key contains name) for short names
  for (const key of sortedKeys) {
    if (key.includes(name) && name.length >= 3) {
      return imageLibrary[key];
    }
  }

  // 4. Word-based matching
  const words = name.split(/[\s\-,\.]+/).filter(w => w.length > 2);
  for (const word of words) {
    if (imageLibrary[word]) {
      return imageLibrary[word];
    }
  }

  // 5. Partial word matching (at least 4 chars)
  for (const word of words) {
    if (word.length >= 4) {
      for (const key of sortedKeys) {
        if (key.includes(word) || word.includes(key)) {
          return imageLibrary[key];
        }
      }
    }
  }

  // 6. Category fallback
  for (const [key, value] of Object.entries(categoryFallbacks)) {
    if (category.includes(key) || name.includes(key)) {
      return value;
    }
  }

  // 7. Generic fallback
  return 'ğŸ½ï¸';
}

